## Functions are implementing Inverse matrix calculation with cache functionality

## This function is preparing main vector of using the matrix

makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setsolve <- function(solve) m <<- solve
        getsolve <- function() m
        list(set = set, get = get,
             setsolve = setsolve,
             getsolve = getsolve)
}


## This function is calculating Inverse of a matrix 
## or uses cached result from previous calculations

